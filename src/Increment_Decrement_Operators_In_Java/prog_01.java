package Increment_Decrement_Operators_In_Java;

public class prog_01 {
    public static void main(String[] args) {

        int a = 10;
        a++;
        System.out.println(a);

        System.out.println("--------");

        int b = 10;
        ++b;
        System.out.println(b);

        System.out.println("--------");


    }
}
