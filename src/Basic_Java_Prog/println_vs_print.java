package Basic_Java_Prog;

public class println_vs_print {
    public static void main(String[] args) {

        //Displays the output on the console and then moves the cursor to the next line.
        System.out.println("Hi How Are You");

        //Displays the output on the console without moving the cursor to the next line.
        System.out.print("Hi How Are You");
    }
}
