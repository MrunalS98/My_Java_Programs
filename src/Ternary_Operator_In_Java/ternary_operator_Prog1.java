package Ternary_Operator_In_Java;

public class ternary_operator_Prog1 {
    public static void main(String[] args) {
        //Evaluate condition and return one on the value
        //similar to if-else condition
        int age = 100;
        String vote = age >= 18 ? "yes" : "No";
        System.out.println(vote);
    }
}
